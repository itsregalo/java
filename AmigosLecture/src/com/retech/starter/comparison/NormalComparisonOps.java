package com.retech.starter.comparison;

public class NormalComparisonOps {
    public static void main(String[] args){
        int hisAge = 20;
        int matesAge = 19;

        System.out.println(hisAge<matesAge); //less than
        System.out.println(hisAge>matesAge); //greater than
        System.out.println(hisAge==matesAge); //equal to
        System.out.println(hisAge!=matesAge); //not equal to
        System.out.println(hisAge<=matesAge); //less or equal
        System.out.println(hisAge>=matesAge); //greater or equal
    }
}
